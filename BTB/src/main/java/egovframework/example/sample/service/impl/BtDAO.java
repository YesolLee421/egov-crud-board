package egovframework.example.sample.service.impl;

import java.util.List;

import egovframework.example.sample.service.BtVO;
import egovframework.example.sample.service.SampleDefaultVO;
import egovframework.example.sample.service.SampleVO;

import egovframework.rte.psl.dataaccess.EgovAbstractDAO;

import org.springframework.stereotype.Repository;


@Repository("btDAO")
public class BtDAO extends EgovAbstractDAO {

	/**
	 * 글을 등록한다.
	 * @param vo - 등록할 정보가 담긴 SampleVO
	 * @return 등록 결과
	 * @exception Exception
	 */
	public String insertBt(BtVO vo) throws Exception {
		return (String) insert("btDAO.insertBt", vo);
	}

	/**
	 * 글을 수정한다.
	 * @param vo - 수정할 정보가 담긴 SampleVO
	 * @return void형
	 * @exception Exception
	 */
	public void updateBt(BtVO vo) throws Exception {
		update("btDAO.updateBt", vo);
	}

	/**
	 * 글을 삭제한다.
	 * @param vo - 삭제할 정보가 담긴 SampleVO
	 * @return void형
	 * @exception Exception
	 */
	public void deleteBt(BtVO vo) throws Exception {
		delete("btDAO.deleteBt", vo);
	}

	/**
	 * 글을 조회한다.
	 * @param vo - 조회할 정보가 담긴 SampleVO
	 * @return 조회한 글
	 * @exception Exception
	 */
	public BtVO selectBt(BtVO vo) throws Exception {
		return (BtVO) select("btDAO.selectBt", vo);
	}

	/**
	 * 글 목록을 조회한다.
	 * @param searchMap - 조회할 정보가 담긴 Map
	 * @return 글 목록
	 * @exception Exception
	 */
	public List<?> selectBtList(SampleDefaultVO searchVO) throws Exception {
		return list("btDAO.selectBtList", searchVO);
	}

	/**
	 * 글 총 갯수를 조회한다.
	 * @param searchMap - 조회할 정보가 담긴 Map
	 * @return 글 총 갯수
	 * @exception
	 */
	public int selectBtListTotCnt(SampleDefaultVO searchVO) {
		return (Integer) select("btDAO.selectBtListTotCnt", searchVO);
	}

}
